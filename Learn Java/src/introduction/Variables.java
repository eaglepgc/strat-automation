package introduction;

public class Variables {

	public static void main(String[] args) {
		/*
		 * Byte
		 * Min: -128
		 * Max: 127
		 * Default: 0
		 */
		byte byteVarible = 100;
		System.out.println("Byte Value:" + byteVarible);
		
		/*
		 * Short
		 * Min: -32,768
		 * Max: 32,767
		 * Default: 0
		 */		
		short shortVarible = 1000;
		System.out.println("Sort Value:" + shortVarible);
		
		/*
		 * int
		 * Min: -2,147,483,648
		 * Max: 2,147,483,647
		 * Default: 0
		 */	
		int intVarible = 100000;
		System.out.println("int Value:" + intVarible);
		
		/*
		 * long
		 * Min: -crazy
		 * Max: crazy
		 * Default: 0
		 */	
		long longVarible = 1000000000L;
		System.out.println("long Value:" +longVarible);
		
		/*
		 * float
		 * Default: 0.0f
		 */	
		float floatVarible = 20.1f;
		System.out.println("float Value:" +longVarible);
		
		/*
		 * double
		 * Default: 0.0d
		 */	
		double doubleVarible = 60.1262;
		System.out.println("float Value:" +doubleVarible);
		
		/*
		 * boolean
		 * false and true
		 * Default false
		 */	
		boolean booleanVarible = true;
		System.out.println("Boolean Value:" +booleanVarible);
		
		
		/*
		 * char
		 * Min:0
		 * Max: 65,535
		 * Default false
		 */	
		char charVarible = 'a';
		System.out.println("Char Value:" +charVarible);


	}

}
